#include "watch_dog.h"

/*********************************************************************************************
* 名称：watchdog_init()
* 功能：看门狗代码初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void watchdog_init(void) 
{ 
  WDCTL  = 0x00;                                                //打开IDLE才能设置看门狗
  WDCTL |= 0x08;                                                //定时器间隔选择,间隔一秒
}

/*********************************************************************************************
* 名称：feet_dog()
* 功能：喂狗操作
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void feet_dog(void)
{ 
  WDCTL = 0xa0;                                                 //清除定时器。当0xA跟随0x5写到这些位，定时器被清除
  WDCTL = 0x50; 
}