#include "led.h"

/*********************************************************************************************
* 名称：led_io_init()
* 功能：LED灯管脚初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void led_io_init(void)
{
  P1SEL &= ~0x03;                                               //配置管脚为通用IO模式
  P1DIR |= 0x03;                                                //配置控制管脚为输出模式
  
  LED0 = OFF;                                                   //初始化LED状态为关闭
  LED1 = OFF;                                                   //初始化LED状态为关闭
}