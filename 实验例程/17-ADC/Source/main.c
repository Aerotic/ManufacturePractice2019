#include "stm32f4xx.h"
#include "string.h"
#include "delay.h"
#include "led.h"
#include "ADC.h"
#include "usart.h"
/*********************************************************************************************
* 全局变量
*********************************************************************************************/
uint32_t ADCvalue = 0;                                          //ADC真实值
float value=0;                                                  //转换后的值
/*********************************************************************************************
* 名称：main()
* 功能：LED驱动实验逻辑代码
* 参数：无
* 返回：无
* 修改：
*********************************************************************************************/
void main(void)
{
  led_init();                                                   //初始化LED控制管脚 
  AdcInit();                                                    //ADC配置初始化
  while(1){  
    ADCvalue=AdcGet(1);                                         //adc值获取
    value =(ADCvalue*(12.0f/4095));                              //标准电压转化
    delay_count(100);                                            //延时
  }
}  