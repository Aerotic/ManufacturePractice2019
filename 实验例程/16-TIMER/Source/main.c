#include "stm32f4xx.h"
#include "delay.h"
#include "led.h"
#include "timer.h"
char led_status = 0;                                            //声明一个表示LED灯状态的变量
/*********************************************************************************************
* 名称：main()
* 功能：LED驱动实验逻辑代码
* 参数：无
* 返回：无
* 修改：
*********************************************************************************************/
void main(void)
{
  led_init();                                                   //初始化LED控制管脚
  timer3_init(5000-1, 16800-1);                                 //初始化定时器3，设置溢出时间为1000ms
  while(1){                                                     //循环体
    if(led_status == 0){                                        //LED灯为状态0
      turn_on(D3);                                              //D3开
      turn_off(D4);                                             //D4关
    }
    else{                                                       //LED灯为状态1
      turn_off(D3);                                             //D3关
      turn_on(D4);                                              //D4开
    }
  }
}  