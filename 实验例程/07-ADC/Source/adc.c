#include "adc.h"

/*********************************************************************************************
* 名称：adc_test()
* 功能：ADC采集
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
int adc_test(void)
{
  unsigned int  value;
  
  APCFG |= 0x10;                                                //模拟 I/O 使能  
  P0SEL |= 0x10;                                                //端口0_4 功能选择外设功能
  P0DIR &= ~0x10;                                               //设置输入模式
  ADCCON3  = 0xB4;                                              //选择AVDD5为参考电压；12分辨率；P0_4  ADC  
  ADCCON1 |= 0x30;                                              //选择ADC的启动模式为手动
  ADCCON1 |= 0x40;                                              //启动AD转化             
  
  while(!(ADCCON1 & 0x80));                                     //等待ADC转化结束  
  value =  ADCL >> 2;
  value |= (ADCH << 6)>> 2;                                     //取得最终转化结果，存入value中  
  return value;
}