#include "stm32f4xx.h"
#include "delay.h"
#include "led.h"
#include "exti.h"
char led_status = 0;                                            //声明一个表示LED灯状态的变量
/*********************************************************************************************
* 名称：main()
* 功能：外部中断实验逻辑代码
* 参数：无
* 返回：无
* 修改：
*********************************************************************************************/
void main(void)
{
  led_init();                                                   //初始化LED控制管脚
  exti_init();                                                  //初始化按键检测管脚
  while(1){                                                     //循环体
    if(led_status == 0){                                        //LED灯为状态0
      turn_on(D1);                                              //D1开
      turn_off(D2);                                             //D2关
    }
    else{                                                       //LED灯为状态1
      turn_off(D1);                                             //D1关
      turn_on(D2);                                              //D2开
    }
  }
}  